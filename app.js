//Операторы присваивания
let age = 18 + 2;
console.log(age);
age += 2; // age = age + 2 
console.log(age);
age -= 2; // age = age - 2 
console.log(age);
age *= 2; // age = age * 2 
console.log(age);
age /= 2; // age = age / 2 
console.log(age);
age ++; // age = age + 1 
console.log(age);
age --; // age = age - 1 
console.log(age);

//Операторы сравнения

const vasya = 20
console.log(age > vasya);
console.log(age >= vasya);
console.log(age < vasya);
console.log(age <= vasya);
console.log(age == vasya);


